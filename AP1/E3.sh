export PATH="$PATH:~/scripts"

# Aquest comandament modifica el PATH del sistema, que és una variable d'entorn 
# que indica al teu sistema operatiu on ha de buscar els comandaments (com ara scripts) 
# quan els executes a la terminal.

# En afegir "~/scripts" al PATH, estàs dient al sistema que també 
# busqui comandaments a la carpeta "scripts" que es troba dins del teu directori 
# personal. Això significa que si tens un script anomenat "mi_script.sh" en aquesta carpeta, 
# podràs executar-lo simplement escrivint "mi_script.sh" a la terminal, 
# sense necessitat d'escriure la ruta completa "~/scripts/mi_script.sh".

# En resum, aquest comandament et permet executar scripts que es troben 
# a la carpeta "~/scripts" de forma més senzilla.